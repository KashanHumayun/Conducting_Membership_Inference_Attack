    private void jButton29ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton29ActionPerformed
        // TODO add your handling code here:
        if(AdminNameField.getText().isEmpty()||AdminAgeField.getText().isEmpty()||
        AdminAddressField.getText().isEmpty()|| AdminPasswordField.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"Please Enter All Fields!");
        }       
        else{
            String Name=AdminNameField.getText().toString().replaceAll(" ","_");
            int Age=parseInt(AdminAgeField.getText().toString().replaceAll(" ","_"));
            String Address=AdminAddressField.getText().toString().replaceAll(" ","_");
            String Password=AdminPasswordField.getText().toString().replaceAll(" ","");
            admin =new Admin(Name,Age,Address,Password);
            try{
                admin.Enter_details_GUI();
            }
            
            catch (IOException ex) {
                JOptionPane.showMessageDialog(null,ex);
            }
        }
    }//GEN-LAST:event_jButton29ActionPerformed

