        String nametemp;
        long idtemp;
        int agetemp;
        String addresstemp; 
        String Statustemp;
        File cus = new File("Customer.txt");
                 
        Scanner Reader = null;
        DefaultTableModel model=(DefaultTableModel)jTable1.getModel();
                
        try {
            Reader = new Scanner(cus);
        } catch (FileNotFoundException ex) {
            JOptionPane.showMessageDialog(rootPane,ex);
        }
        while(Reader.hasNext()){
            
            idtemp = Reader.nextLong();
            nametemp=Reader.next();
            agetemp=Reader.nextInt();
            addresstemp=Reader.next();
            Statustemp=Reader.next();
            String[] datarow={idtemp+"",nametemp,agetemp+"",addresstemp,Statustemp};
            model.addRow(datarow);
        }
        Reader.close();     
        MainPanel.removeAll();
        MainPanel.add(ShowAllCustomersPanel);
        MainPanel.revalidate();
        MainPanel.repaint();
